//Write a progarm that takes a number from 0 to 5 and prints its spelling,
//if the number is greater than 5 print entered number is greater than 5
void main(){
  var n=4;
  if(n==0){
    print("Zero");
  }else if(n==1){
    print("One");
  }else if(n==2){
    print("Two");
  }else if(n==3){
    print("Three");
  }else if(n==4){
    print("Four");
  }else if(n==5){
    print("Five");
  }else{
    print("Entered number is greater than 5");
  }
}